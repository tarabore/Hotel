    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre para el Trabajador");
            txtNombre.requestFocus();
            return;
        }
        if (txtApellido.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido para el Trabajador");
            txtApellido.requestFocus();
            return;
        }
        if (txtApellidoMadre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido para el Trabajador");
            txtApellidoMadre.requestFocus();
            return;
        }
        if (txtNumDoc.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Número de Doc para el Trabajador");
            txtNumDoc.requestFocus();
            return;
        }
        if (txtSueldo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Sueldo para el trabajador");
            txtSueldo.requestFocus();
            return;
        }
        if (txtLogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un login para el trabajador");
            txtLogin.requestFocus();
            return;
        }
        if (txtPassword.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un password para el trabajador");
            txtPassword.requestFocus();
            return;
        }
        Trabajador trabajador = new Trabajador();
        TrabajadorDAO trabajadorDAO = new TrabajadorDAO();
        trabajador.setNombre(txtNombre.getText());
        trabajador.setApellido(txtApellido.getText());
        trabajador.setApellidoMadre(txtApellidoMadre.getText());
        int seleccionado = cboTipoDoc.getSelectedIndex();
        trabajador.setTipoDoc((String) cboTipoDoc.getItemAt(seleccionado));
        trabajador.setNumDoc(txtNumDoc.getText());
        trabajador.setDireccion(txtDireccion.getText());
        trabajador.setTelefono(txtTelefono.getText());
        trabajador.setEmail(txtEmail.getText());
        trabajador.setSueldo(Double.parseDouble(txtSueldo.getText()));
        seleccionado = cboAcceso.getSelectedIndex();
        trabajador.setAcceso((String) cboAcceso.getItemAt(seleccionado));
        trabajador.setLogin(txtLogin.getText());
        trabajador.setPassword(txtPassword.getText());
        seleccionado = cboEstado.getSelectedIndex();
        trabajador.setEstado((String) cboEstado.getItemAt(seleccionado));
        if (accion.equals("guardar")) {
            if (trabajadorDAO.insertar(trabajador)) {
                JOptionPane.showMessageDialog(rootPane, "el trabajador fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            trabajador.setIdpersona(Integer.parseInt(txtIdPersona.getText()));
            if (trabajadorDAO.editar(trabajador)) {
                JOptionPane.showMessageDialog(rootPane, "El Trabajador fue Editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

