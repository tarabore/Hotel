    private void tblListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListadoMouseClicked
        btnGuardar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";
        int fila = tblListado.rowAtPoint(evt.getPoint());
        txtIdPersona.setText(isNull(tblListado.getValueAt(fila, 0), ""));
        txtNombre.setText(isNull(tblListado.getValueAt(fila, 1), ""));
        txtApellido.setText(isNull(tblListado.getValueAt(fila, 2), ""));
        txtApellidoMadre.setText(isNull(tblListado.getValueAt(fila, 3), ""));
        cboTipoDoc.setSelectedItem(isNull(tblListado.getValueAt(fila, 4), ""));
        txtNumDoc.setText(isNull(tblListado.getValueAt(fila, 5), ""));
        txtDireccion.setText(isNull(tblListado.getValueAt(fila, 6), ""));
        txtTelefono.setText(isNull(tblListado.getValueAt(fila, 7), ""));
        txtEmail.setText(isNull(tblListado.getValueAt(fila, 8), ""));
        txtSueldo.setText(isNull(tblListado.getValueAt(fila, 9), ""));
        cboAcceso.setSelectedItem(isNull(tblListado.getValueAt(fila, 10), ""));
        txtLogin.setText(isNull(tblListado.getValueAt(fila, 11), ""));
        txtPassword.setText(isNull(tblListado.getValueAt(fila, 12), ""));
        cboEstado.setSelectedItem(isNull(tblListado.getValueAt(fila, 13), ""));
    }//GEN-LAST:event_tblListadoMouseClicked

